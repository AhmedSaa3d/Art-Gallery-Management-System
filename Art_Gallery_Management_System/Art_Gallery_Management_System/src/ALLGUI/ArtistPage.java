     private Boolean check_add_photo_data()
    {
        if(titleofphototetxtfield.getText().equals("") || yearmadetextfield.getText().equals("") ||priceindollartextfield.getText().equals("") ||categorytextfield.getText().equals("") ||photoplacelabel.getIcon() == null )
        {
           JOptionPane.showMessageDialog(this,"Empty Fields.","Message", JOptionPane.WARNING_MESSAGE);
            return false; 
        }
        
      return true;
    }
    
    private Boolean check_add_photo_name(String newname)
    {
        for(int i = 0 ;i<ReadWriteDataFromFiles.artworksize ; i++)
            if(ReadWriteDataFromFiles.artwork[i].getTitle().equals(newname))
            {
                JOptionPane.showMessageDialog(this,"this is name is used before.","Message", JOptionPane.ERROR_MESSAGE);
                return false;
            }
    return true;
    }
     
     private void getartistimage()
    {
        for(int i = 0 ;i < ReadWriteDataFromFiles.artworksize ; i++)
        {
            if(ReadWriteDataFromFiles.artwork[i].getIdArtist() == LOGINFORM.SignInid)
            {
                artistartwork[artworksize] = ReadWriteDataFromFiles.artwork[i];
                artworksize++;
            }
        }
    }
    
    private ImageIcon resizeImage(String imagepath , byte[] pic,JLabel x)
    {
        ImageIcon myimg = null;
        if(imagepath !=null)
            myimg = new ImageIcon(imagepath);
        else
            myimg = new ImageIcon(pic);
        Image img = myimg.getImage();
        Image img2 = img.getScaledInstance(x.getWidth(),x.getHeight(),Image.SCALE_SMOOTH);
        
        ImageIcon imageicon = new ImageIcon(img2);
        
    return imageicon;
    }
    
    
