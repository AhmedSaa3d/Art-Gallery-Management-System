    private void Save__photo_jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Save__photo_jButton1MouseClicked

        if(check_add_photo_data() && check_add_photo_name(titleofphototetxtfield.getText()) )
        {
            ARTWORK newartwork = new ARTWORK();
            newartwork.setId(ReadWriteDataFromFiles.artworksize+1);
            newartwork.setTitle(titleofphototetxtfield.getText());
            newartwork.setIdArtist(LOGINFORM.SignInid);
            newartwork.setIdCustomer(0);//not bought yet
            
            newartwork.setYear(Integer.parseInt(yearmadetextfield.getText()));
            newartwork.setPrice(Integer.parseInt(priceindollartextfield.getText()));
            newartwork.setTypeOfArt(categorytextfield.getText());
            newartwork.setPath(addimagepath);
            
            JOptionPane.showMessageDialog(this,"Data Saved Sir.","Message", JOptionPane.OK_OPTION);
            
            ReadWriteDataFromFiles.addNewartwork(newartwork);
            
            addphotoid.setText("" + (ReadWriteDataFromFiles.artworksize+1));
            titleofphototetxtfield.setText("");
            yearmadetextfield.setText("");
            priceindollartextfield.setText("");
            categorytextfield.setText("");
            addimagepath = "";
            photoplacelabel.setIcon(null);
            
            //add this new photo to the portfolio
            artistartwork[artworksize] = newartwork;
            artworksize++;
            set_portfolio_data();
            setprofiledata();
        }

    }//GEN-LAST:event_Save__photo_jButton1MouseClicked

    private void Choose_Photo_jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Choose_Photo_jButton2ActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images","jpg","png");
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION)
        {
            File selectedfile = file.getSelectedFile();
            addimagepath = selectedfile.getAbsolutePath();
            photoplacelabel.setIcon(resizeImage(addimagepath, null,photoplacelabel));
        }
        else
        {
            JOptionPane.showMessageDialog(this,"No File Chooser","Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Choose_Photo_jButton2ActionPerformed

