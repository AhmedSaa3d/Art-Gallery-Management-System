    private void setprofiledata()
    {
         int getid = LOGINFORM.SignInid - 1;
        
         ARTIST artist = new ARTIST();
                artist = (ARTIST) ReadWriteDataFromFiles.pearsons[getid].get_data();
        
        setidlabel.setText("" + artist.getId());
        setnamelabel1.setText(artist.getName());
        setagelabel.setText("" + artist.getAge());
        setnationalitylabel.setText(artist.getNationality());
        String arr[] = artist.getStyleOfArts();
        String styleofart = "";
        for(int i = 0 ;i < artist.getnOfStyleOfArts(); i++)
        {
               styleofart +=arr[i] + '-';       
        }
        setstyleofartslabel.setText(styleofart);
        
        int totalearnedmoney = 0 , totalboughtphotos = 0;
        for(int i = 0 ;i < artworksize ;i++)
            if(artistartwork[i].getIdCustomer() > 0 )
            {
                totalearnedmoney += artistartwork[i].getPrice();
                totalboughtphotos++;
            }
        settotalearnedmoneylabel.setText("" + totalearnedmoney + " $");
        setnofboughtphotolabel1.setText("" + totalboughtphotos);
        settnofphotolabel.setText("" + artworksize);
    }
    
