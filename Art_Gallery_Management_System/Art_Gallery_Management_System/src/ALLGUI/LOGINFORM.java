    public String checkdata()
    {
        
        if(username.equals("admin") && password.equals("123456"))
        {
               return "admin";
        }
        for(int i=0 ; i<ReadWriteDataFromFiles.pearsonssize ; i++)
        
            if(ReadWriteDataFromFiles.pearsons[i].getName().equals(username) &&ReadWriteDataFromFiles.pearsons[i].getPassword().equals(password))
            {
                ///fe one add here to use in array sub 1 from signinid//
                LOGINFORM.SignInid = i+1;
                if(ReadWriteDataFromFiles.pearsons[i] instanceof CUSTOMER )
                     return "customer";
                else if(ReadWriteDataFromFiles.pearsons[i] instanceof ARTIST )
                return "artist";
            }
        return "fail";
    }
    
