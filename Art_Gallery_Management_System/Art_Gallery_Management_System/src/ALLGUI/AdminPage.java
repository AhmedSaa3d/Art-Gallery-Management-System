    private ImageIcon resizeImage(String imagepath , byte[] pic,JLabel x)
    {
        ImageIcon myimg = null;
        if(imagepath !=null)
            myimg = new ImageIcon(imagepath);
        else
            myimg = new ImageIcon(pic);
        Image img = myimg.getImage();
        Image img2 = img.getScaledInstance(x.getWidth(),x.getHeight(),Image.SCALE_SMOOTH);
        
        ImageIcon imageicon = new ImageIcon(img2);
        
    return imageicon;
    }
    
    
