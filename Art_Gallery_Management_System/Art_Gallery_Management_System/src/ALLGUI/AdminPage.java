    private Boolean check_add_photo_data()
    {
        if(titleofphototetxtfield1.getText().equals("") || yearmadetextfield1.getText().equals("") ||priceindollartextfield1.getText().equals("") ||categorytextfield1.getText().equals("") ||photoplacelabel1.getIcon() == null )
        {
           JOptionPane.showMessageDialog(this,"Empty Fields.","Message", JOptionPane.WARNING_MESSAGE);
            return false; 
        }
        
      return true;
    }
    
    private Boolean check_add_photo_name(String newname)
    {
        for(int i = 0 ;i<ReadWriteDataFromFiles.artworksize ; i++)
            if(ReadWriteDataFromFiles.artwork[i].getTitle().equals(newname))
            {
                JOptionPane.showMessageDialog(this,"this is name is used before.","Message", JOptionPane.ERROR_MESSAGE);
                return false;
            }
    return true;
    }
     
    
    
